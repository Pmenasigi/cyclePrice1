package com.in.model;

public class Wheels {

	private float spokes;
    private float rim;
    private float tube;
    private float tyre;
	public float getSpokes() {
		return spokes;
	}
	public void setSpokes(float spokes) {
		this.spokes = spokes;
	}
	public float getRim() {
		return rim;
	}
	public void setRim(float rim) {
		this.rim = rim;
	}
	public float getTube() {
		return tube;
	}
	public void setTube(float tube) {
		this.tube = tube;
	}
	public float getTyre() {
		return tyre;
	}
	public void setTyre(float tyre) {
		this.tyre = tyre;
	}
    
    
}
