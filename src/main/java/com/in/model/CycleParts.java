package com.in.model;



public class CycleParts {

	private float frame;
	private float handleBar;
	private float seating;
	private Wheels wheels;
	private float chainAssembly;
	public float getFrame() {
		return frame;
	}
	public void setFrame(float frame) {
		this.frame = frame;
	}
	public float getHandleBar() {
		return handleBar;
	}
	public void setHandleBar(float handleBar) {
		this.handleBar = handleBar;
	}
	public float getSeating() {
		return seating;
	}
	public void setSeating(float seating) {
		this.seating = seating;
	}
	public Wheels getWheels() {
		return wheels;
	}
	public void setWheels(Wheels wheels) {
		this.wheels = wheels;
	}
	public float getChainAssembly() {
		return chainAssembly;
	}
	public void setChainAssembly(float chainAssembly) {
		this.chainAssembly = chainAssembly;
	}
	
}
